        <ul>
          <li>
            <h3>Barot Bellingham</h3>
            <p><img src="images/artists/Barot_Bellingham_tn.jpg" alt="Barot Bellingham">Barot has just finished his final year at The Royal Academy of Painting and Sculpture, where he excelled in glass etching paintings and portraiture. Hailed as one of the most diverse artists of his generation, Barot is equally as skilled with watercolors as he is with oils.</p>
          </li>
          <li>
            <h3>Jonathan G. Ferrar II</h3>
            <p><img src="images/artists/Jonathan_Ferrar_tn.jpg" alt="Jonathan Ferrar">Labeled as “The Artist to Watch in 2016” by the London Review, Johnathan has already sold one of the highest priced commissions paid to an art student, ever on record. The piece, entitled Gratitude Resort, a work in oil and mixed media, was sold for $750,000.</p>
          </li>
          <li>
            <h3>Hillary Hewitt Goldwynn-Post</h3>
            <p><img src="images/artists/Hillary_Goldwynn_tn.jpg" alt="Hillary Goldwynn">Hillary is a sophomore art sculpture student at New York University, and has already won all the major international prizes for new sculptors, including the Divinity Circle, the International Sculptor’s Medal, and the Academy of Paris Award. Hillary’s CAC exhibit features paintings that contain only water images including waves, deep sea, and river.</p>
          </li>
          <li>
            <h3>Hassum Harrod</h3>
            <p><img src="images/artists/Hassum_Harrod_tn.jpg" alt="Hassum Harrod">The Art College in New Dehli has sponsored Hassum for his entire undergraduate career at the university, seeing great promise in his contemporary paintings of landscapes - that use equal parts muted and vibrant tones. Hassum will be speaking on “The use and absence of color in modern art”.</p>
          </li>
          <li>
            <h3>Jennifer_Jerome</h3>
            <p><img src="images/artists/Jennifer_Jerome_tn.jpg" alt="Jennifer Jerome">A native of New Orleans, much of Jennifer’s work has centered around abstract images that depict flooding and rebuilding, having grown up as a teenager in the post-flood years. Despite the sadness of devastation and lives lost.</p>
          </li>
          <li>
            <h3>LaVonne L. LaRue</h3>
            <p><img src="images/artists/LaVonne_LaRue_tn.jpg" alt="LaVonne LaRue">LaVonne’s giant-sized paintings all around Chicago tell the story of love, nature, and conservation - themes that are central to her heart. LaVonne will share her love and skill of graffiti art on Monday’s schedule, as she starts the painting of a 20-foot high wall in the Rousseau Room of Hotel Contempo in front of a standing-room only audience in Art in Unexpected Places.</p>
          </li>
          <li>
            <h3>Constance Olivia Smith</h3>
            <p><img src="images/artists/Constance_Smith_tn.jpg" alt="Constance Smith">Constance received the Fullerton-Brighton-Norwell Award for Modern Art for her mixed-media image of a tree of life, with jewel-adorned branches depicting the arms of humanity, and precious gemstone-decorated leaves representing the spouting buds of togetherness.</p>
          </li>
          <li>
            <h3>Riley Rudolph Rewington</h3>
            <p><img src="images/artists/Riley_Rewington_tn.jpg" alt="Riley Rewington">A first-year student at the Roux Academy of Art, Media, and Design, Riley is already changing the face of modern art at the university. Riley’s exquisite abstract pieces have no intention of ever being understood, but instead beg the viewer to dream, create, pretend, and envision with their mind’s eye. Riley will be speaking on the “Art of Abstract” during Thursday’s schedule.</p>
          </li>
          <li>
            <h3>Xhou Ta</h3>
            <p><img src="images/artists/Xhou_Ta_tn.jpg" alt="Xhou Ta">A senior at the China International Art University, Xhou has become well-known for his miniature sculptures, often the size of a rice granule, that are displayed by rear projection of microscope images on canvas. Xhou will discuss the art and science behind his incredibly detailed works of art.</p>
          </li>
        </ul>
